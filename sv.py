class SiVi:
	def __init__(self,MSSV,TEN,KHOA):
		self.MSSV = MSSV
		self.TEN = TEN
		self.KHOA = KHOA
	def getmssv(self):
		return self.MSSV
	def getten(self):
		return self.TEN
	def getkhoa(self):
		return self.KHOA
	def setmssv(self,MSSV):
		self.MSSV = MSSV
	def setten(self,TEN):
		self.TEN = TEN
	def setkhoa(self,KHOA):
		self.KHOA = KHOA

